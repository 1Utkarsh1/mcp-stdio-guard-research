import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const snapshotDir = path.resolve(
  root,
  process.argv[2] ?? 'artifact/results/v1.0.0-paper-baseline'
);
const rawResultsPath = path.join(snapshotDir, 'raw-results.json');
const tableDir = path.join(root, 'paper', 'tables');
const rawResults = JSON.parse(await fs.readFile(rawResultsPath, 'utf8'));

await fs.mkdir(tableDir, { recursive: true });
await fs.writeFile(
  path.join(tableDir, 'detection-matrix.tex'),
  detectionMatrixTable(rawResults)
);
await fs.writeFile(
  path.join(tableDir, 'runtime-overhead.tex'),
  runtimeOverheadTable(rawResults)
);

console.log(`Generated paper tables from ${path.relative(root, rawResultsPath)}`);

function detectionMatrixTable(result) {
  const scenarioRows = aggregateScenarios(result.runs);
  const lines = [
    '% Generated by evaluation/generate-paper-tables.js from artifact/results/v1.0.0-paper-baseline/raw-results.json.',
    '\\begin{table*}[t]',
    '\\centering',
    '\\small',
    '\\caption{Synthetic MCP stdio fault detection matrix for the pinned v1.0.0 baseline.}',
    '\\label{tab:detection-matrix}',
    '\\begin{tabularx}{\\textwidth}{p{0.24\\textwidth}p{0.30\\textwidth}p{0.14\\textwidth}X}',
    '\\toprule',
    'Scenario & Injected fault & Correct profiles & Detected issue codes \\\\',
    '\\midrule'
  ];

  for (const row of scenarioRows) {
    lines.push([
      latexText(row.scenario),
      latexText(row.injectedFault),
      `${row.detectedProfiles}/${row.totalProfiles}`,
      row.issueCodes.length ? row.issueCodes.map(latexCode).join(', ') : 'none'
    ].join(' & ') + ' \\\\');
  }

  lines.push(
    '\\bottomrule',
    '\\end{tabularx}',
    '\\end{table*}',
    ''
  );
  return lines.join('\n');
}

function runtimeOverheadTable(result) {
  const profileRows = aggregateProfiles(result.runs);
  const summary = result.summary;
  const lines = [
    '% Generated by evaluation/generate-paper-tables.js from artifact/results/v1.0.0-paper-baseline/raw-results.json.',
    '\\begin{table}[t]',
    '\\centering',
    '\\small',
    '\\caption{Observed wall-clock runtime for the pinned v1.0.0 evaluation runs.}',
    '\\label{tab:runtime-overhead}',
    '\\begin{tabular}{lrrrr}',
    '\\toprule',
    'Profile & Runs & Median ms & P95 ms & Max ms \\\\',
    '\\midrule'
  ];

  for (const row of profileRows) {
    lines.push([
      latexText(row.profile),
      String(row.runs),
      formatNumber(row.medianMs),
      formatNumber(row.p95Ms),
      formatNumber(row.maxMs)
    ].join(' & ') + ' \\\\');
  }

  lines.push(
    '\\midrule',
    [
      'All',
      String(result.runs.length),
      formatNumber(summary.medianRuntimeMs),
      formatNumber(summary.p95RuntimeMs),
      formatNumber(Math.max(...result.runs.map((run) => run.durationMs)))
    ].join(' & ') + ' \\\\',
    '\\bottomrule',
    '\\end{tabular}',
    '\\end{table}',
    ''
  );
  return lines.join('\n');
}

function aggregateScenarios(runs) {
  const byScenario = new Map();
  for (const run of runs) {
    if (!byScenario.has(run.scenario)) {
      byScenario.set(run.scenario, {
        scenario: run.scenario,
        injectedFault: run.injectedFault,
        totalProfiles: 0,
        detectedProfiles: 0,
        issueCodes: new Set()
      });
    }
    const row = byScenario.get(run.scenario);
    row.totalProfiles += 1;
    if (run.detected) row.detectedProfiles += 1;
    for (const code of run.issueCodes) row.issueCodes.add(code);
  }

  return [...byScenario.values()].map((row) => ({
    ...row,
    issueCodes: [...row.issueCodes].sort()
  }));
}

function aggregateProfiles(runs) {
  const byProfile = new Map();
  for (const run of runs) {
    const values = byProfile.get(run.profile) ?? [];
    values.push(run.durationMs);
    byProfile.set(run.profile, values);
  }

  return [...byProfile.entries()].map(([profile, values]) => {
    const sorted = values.toSorted((a, b) => a - b);
    return {
      profile,
      runs: sorted.length,
      medianMs: percentile(sorted, 0.5),
      p95Ms: percentile(sorted, 0.95),
      maxMs: sorted.at(-1)
    };
  }).sort((a, b) => a.profile.localeCompare(b.profile));
}

function percentile(sortedValues, p) {
  if (!sortedValues.length) return 0;
  const index = Math.ceil(p * sortedValues.length) - 1;
  return sortedValues[Math.max(0, Math.min(index, sortedValues.length - 1))];
}

function formatNumber(value) {
  return Number(value).toFixed(3);
}

function latexText(value) {
  return String(value)
    .replaceAll('\\', '\\textbackslash{}')
    .replaceAll('&', '\\&')
    .replaceAll('%', '\\%')
    .replaceAll('$', '\\$')
    .replaceAll('#', '\\#')
    .replaceAll('_', '\\_')
    .replaceAll('{', '\\{')
    .replaceAll('}', '\\}')
    .replaceAll('~', '\\textasciitilde{}')
    .replaceAll('^', '\\textasciicircum{}');
}

function latexCode(value) {
  return `\\texttt{${latexText(value)}}`;
}
